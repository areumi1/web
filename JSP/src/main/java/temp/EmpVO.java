package temp;

public class EmpVO {
   
   int empno;
   String name;
   int deptno;
   String job;
   int sal;
   
   
public int getEmpno() {
	return empno;
}
public void setEmpno(int empno) {
	this.empno = empno;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getDeptno() {
	return deptno;
}
public void setDeptno(int deptno) {
	this.deptno = deptno;
}
public String getJob() {
	return job;
}
public void setJob(String job) {
	this.job = job;
}
public int getSal() {
	return sal;
}
public void setSal(int sal) {
	this.sal = sal;
}
   
   
}